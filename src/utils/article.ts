type Article = {
    title: string;
    link: string;
    source: string;
    snippet?: string;
    imageUrl: string;
    date: string;
    position: number;
    section?: string;

  };
  
  type TopicData = {
    topic: string;
    articles: Article[];
  };
  
type FlattenedArticle = {
    title: string;
    link: string;
    source: string;
    snippet: string;
    imageUrl: string;
    category: string;
  };
  
  export function flattenArticles(data: TopicData[]): FlattenedArticle[] {
    return data.flatMap(item => 
      item.articles.map(article => ({
          title: article.title,
          link: article.link,
          source: article.source,
          imageUrl: article.imageUrl,
        
          snippet: article.snippet || "",
        category: item.topic
      }))
    );
  }
  
